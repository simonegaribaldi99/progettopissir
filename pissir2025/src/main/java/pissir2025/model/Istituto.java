package pissir2025.model;

public class Istituto {
	
	private int idIstituto;
	private String nome;
	
	public Istituto(int idIstituto, String nome) {
		super();
		this.idIstituto = idIstituto;
		this.nome = nome;
	}
	
	
	public int getIdIstituto() {
		return idIstituto;
	}
	
	public String getNome() {
		return nome;
	}


	
	
	

}
